// scripts/emit-metrics-tex.js
//
// PURE, OFFLINE. Derives every numeric fact the CVs used to hardcode and writes them as
// LaTeX \newcommand macros into cv/**/generated/metrics.tex. The .tex shells \input this
// file and reference the macros, so editing a number happens once (in the .bib or in the
// hand-authored static block of data/stats.json) and both PDFs update on the next build.
//
// Counts come BY BIB TYPE from cv/publications.bib (journal papers = @article, etc.);
// citation/funding/service figures come from data/stats.json. Never hand-edit the output.

import { writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { loadData, pubCounts, ROOT } from './lib/data.js';

const { stats, publications } = loadData();
const counts = pubCounts(publications);

const funding = stats.static?.fundingAwarded?.value ?? 0;

// LaTeX macro name -> value. Keep names letters-only (TeX requirement).
const macros = {
  // publication counts, derived by bib type
  journalpapers: counts.journalPapers,
  conferences: counts.conferences,
  datasets: counts.datasets,
  // ORCID works total, kept available but deliberately NOT relabeled as journal papers
  pubcount: stats.publications?.count ?? counts.total,
  // citation metrics from Scholar (via stats.json, last-known-good)
  citations: stats.scholar?.citations ?? 0,
  hindex: stats.scholar?.hIndex ?? 0,
  itenindex: stats.scholar?.i10Index ?? 0,
  // GitHub
  commits: stats.github?.commits ?? 0,
  repos: stats.github?.repos ?? 0,
  // hand-authored service / mentoring facts (static island of stats.json)
  invitedtalks: stats.static?.invitedTalks ?? 0,
  peerreviews: stats.static?.peerReviews ?? stats.orcid?.reviews ?? 0,
  nsfreviews: stats.static?.['NSFProposal Reviews'] ?? 0,
  mentees: stats.static?.menteesSupervised ?? 0,
  // funding, formatted with thousands separators (no leading $ — the .tex supplies it)
  funding: funding.toLocaleString('en-US'),
};

const header = [
  '% cv/**/generated/metrics.tex',
  '% GENERATED by scripts/emit-metrics-tex.js — DO NOT EDIT BY HAND.',
  '% Source: data/stats.json + cv/publications.bib. Regenerate with `npm run metrics`.',
  `% Last generated: ${new Date().toISOString()}`,
  '',
].join('\n');

const body = Object.entries(macros)
  .map(([name, value]) => `\\newcommand{\\${name}}{${value}}`)
  .join('\n');

const contents = `${header}${body}\n`;

for (const rel of ['cv/onepage/generated/metrics.tex', 'cv/biosketch/generated/metrics.tex']) {
  writeFileSync(join(ROOT, rel), contents, 'utf8');
  console.log(`wrote ${rel}`);
}

console.log(
  `metrics: journal=${counts.journalPapers} conf=${counts.conferences} ` +
    `datasets=${counts.datasets} citations=${macros.citations} peerReviews=${macros.peerreviews}`,
);
